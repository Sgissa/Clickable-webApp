function loadData(){
    $.each(ITEMS, function(index, item){
        $("#app").append(`<div class="content-container" >
        <div id="${index}"  class="item-holder">
        <h4 class="game-name">${item.itemTitle}</h4>
          <img src="images/${item.itemThumbImg}" alt="${item.itemThumbImg}" />
          <div class="quick-brief">
          <div class="brief-des">
            ${item.itemBriefDes}
          </div>
        </div>
        </div>
        
      </div>`);
    });

    initListeners();
}

function initListeners(){
    $(".item-holder").click(function(e){
        let itemIndex = e.currentTarget.id;
        console.log(itemIndex);
        $("#app").html(`<div class="item-container">
        <h2>${ITEMS [itemIndex].itemTitle}</h2>
        <h4 class="item-name">Axe</h4>
        <img src="images/${ITEMS[itemIndex].itemFullImg}" alt="axe-image" />
        <div class="itemDes">
          ${ITEMS[itemIndex].itemDescription}
        </div>
        <div class="close">Close</div></div>`);

        addCloselistener();
    });
}

function addCloselistener(){
   $(".close").click(function(){
      $("#app").html("");
      loadData();
   });
}

$(document).ready(function(){
    loadData();
});